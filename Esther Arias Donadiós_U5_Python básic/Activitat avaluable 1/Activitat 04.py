# Operadors “is”, “not”, “in” en Python 3:
# Son operadors lògics que poden tornar valors booleans (“True” o “False”), per exemple:
x = 4
y = 10
a = [1, 3, 5]

5 in a # -> True , porque el número está en la lista.
not(x>y) # -> True, porque x NO es mayor que y.
x is y # -> False, porque x no ES y